// Browser & Node.js
// Try and Catch


// Try and catch are used to handle errors
// Try is used to execute a block of code
// Catch is used to handle the error
// If an error occurs in the try block, the catch block is executed
// If no error occurs, the catch block is skipped

try
{
    throw new Error("Error 666-6969");
}
catch(error)
{
    console.log(error);
}


